/**
 * Рассчитывает процент освещенности лунного диска.
 * Используется упрощенная формула на основе синуса.
 * @param phase - Текущее значение фазы Луны (от 0.0 до 1.0).
 * @returns Процент освещенности (от 0 до 100), округленный до целого числа.
 */
export const getMoonIllumination = (phase: number) => {
  // Формула основана на том, что освещенность меняется синусоидально
  // от 0% (новолуние) до 100% (полнолуние) и обратно.
  const illumination = Math.abs(Math.sin(Math.PI * phase));
  return Math.round(illumination * 100);
}
